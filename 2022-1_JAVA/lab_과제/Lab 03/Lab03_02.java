//본인은 이 소스파일을 다른 사람의 소스를 복사하지 않고 직접 작성하였습니다.
public class Lab03_02 {

	public static void main(String[] args) {
		System.out.println("abs(-23) : " + Math.abs(-23));
		System.out.println("ceil(5.5) : " +Math.ceil(5.5));
		System.out.println("cos(0.0) : " +Math.cos(0.0));
		System.out.println("exp(2.0) : " +Math.exp(2.0));
		System.out.println("floor(5.5) : " +Math.floor(5.5));
		System.out.println("log(Math.E) : " +Math.log(Math.E));
		System.out.println("max(2.7, 3.7) : " +Math.max(2.7, 3.7));
		System.out.println("min(2.7, 3.7) : " +Math.min(2.7, 3.7));
		System.out.println("pow(2.0, 7.0) : " +Math.pow(2.0, 7.0));
		System.out.println("sin(0.0) : " +Math.sin(0.0));
		System.out.println("sqrt(900.0) : " +Math.sqrt(900.0));
		System.out.println("tan(0.0) : " +Math.tan(0.0));

	}

}
